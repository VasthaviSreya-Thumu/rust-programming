// Define constants at the top level
const MAX_POINTS: u32 = 100;
const PI: f64 = 3.14159;
const GREETING: &str = "Hello, Rust!";
const IS_ENABLED: bool = true;

fn main() {
    println!("MAX_POINTS: {}", MAX_POINTS);       // Integer constant
    println!("PI: {}", PI);                       // Floating-point constant
    println!("GREETING: {}", GREETING);           // String slice constant
    println!("IS_ENABLED: {}", IS_ENABLED);       // Boolean constant
}
